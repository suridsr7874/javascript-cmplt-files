// String
let empName = 'John';
console.log(`Emp Name : ${empName} Type : ${typeof empName}`);

// Number
let empAge = 45;
console.log(`Emp Age : ${empAge} Type : ${typeof empAge}`);

// Boolean
let isActive = true;
console.log(`isActive : ${isActive} Type : ${typeof isActive}`);

// Null
let empSalary = null;
console.log(`empSalary : ${empSalary} Type : ${typeof empSalary}`);

// Reassignment
let a;
console.log(`value : ${a} Type : ${typeof a}`);

a = 10;
console.log(`value : ${a} Type : ${typeof a}`);

a = 'test';
console.log(`value : ${a} Type : ${typeof a}`);

a = true;
console.log(`value : ${a} Type : ${typeof a}`);

a = null;
console.log(`value : ${a} Type : ${typeof a}`);